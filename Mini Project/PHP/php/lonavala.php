            <h1>Lakes and dams of Lonavala </h1>
            <h5>POSTED ON JULY 31, 2017 BY NAMAN KEJRIWAL</h5>
        <hr>
        </div>
        <div class="is-divider>"></div>
        <img src="images/lonavala.jpg"  class="image" height="500px" width="900px">
        <div class="content">
            <p>There are many things which you find in the green covered mountainous regions and never-ending valleys. Among them, the<br> most capturing site is of lakes and small waterfalls that adorn it. Along with it comes the fact that humans have made artificial lakes for sustenance. Huge dams and engineering marvels created so that we can survive in any place of our choice without breaking a sweat. Lonavala being one such place has many small lakes. Some made naturally and some by ingenious human minds. Either way, it is a visual treat to be around one of them. Few things can match the peace one experiences near water, and that’s something Lonavala has in abundance. A few of the lakes and dams include the following.
            </p>
        </div>
        <h4>Bhushi Dam</h4>
        <img src="images/lonavala2.jpg" height="500px" width="900px" class="image">
        <div class="content">
            <p>
                    Bhushi Dam is a masonry dam on the Indrayani River. Bhushi dam is a place which is a hit amongst all tourists visiting Lonavala. It is indeed a sight to watch the water cascade over the dam walls during the monsoon. The place is generally throbbing with eager crowds but that should not deter anyone from visiting the dam. Swimming is prohibited but one can enjoy the serenity that the water provides. A major attraction of Lonavala, make sure that you pay this spot a visit.
</p>
                </div>
                <h4>Valvan Dam</h4>
        <img src="images/lonavala3.jpg" height="500px" width="900px" class="image">
        <div class="content">
            <p>
                  
                  Valvan Dam has a garden at its foot and is a popular evening spot 2 km from the town. The dam supplies water to the Khopoli power station at the foothills of the Sahyadris for generating electricity. The Kundali River feeds into the dam’s reservoir.<br>
                  Situated within the confines of the city, this dam is a perfect place to spend an evening in Lonavala. Having greenery and water all around, make sure you carry your cameras with you. The most amazing selfies can be snapped here. Over the years the scenery has made this a picnic spot for tourists.
                </p>
        </div>
        <h4>Tungarli Lake and Dam
            </h4>
        <img src="images/lonavala4.jpg" height="500px" width="900px" class="image">
        <div class="content">
            <p>
                    The Tungarli Dam is built alongside Tungarli lake in the region of Rajmachi. Camping along the lake side is allowed and must be done without a thought. The lake gives a spectacular view of the entire city of Lonavala in the evenings. It is recommended to visit it during the monsoon. The place is serene and like all the place in Lonavala, a feast for nature lovers. The untainted air of the air is bound to leave you refreshed.
                   </p>
