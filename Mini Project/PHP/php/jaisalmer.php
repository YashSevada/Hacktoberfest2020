                    <h1>Jaisalmer Desert Camping</h1>
                    <h5>POSTED ON SEPTEMBER 13, 2019 BY AMAN BHARDWAJ
                        </h5>
                <hr>
                </div>
                <div class="is-divider>"></div>
        <img src="images/jaisalmer3.jpg"  class="image">
        <div class="content">
            <p>If you have grown up watching Satyajit Ray’s ‘Feluda’, then you would know why Jaisalmer is important. Home to the famous ‘Sonar Kella’ (Fort of Gold), it’s a dream of every travel junkie to visit this place. Jaisalmer, an Arabian fairytale that’s going to take you to a thousand miles away from the monotonous life. 

                    Located in the sandy dunes of Rajasthan, Jaisalmer is a place famous for its forts. Nicknamed as the “Golden City” this city is adorned with the famous Jaisalmer Fort. The city is constructed using yellowish sandstone which gives the city a rugged look. Although Jaisalmer is mainly famous for its Forts, another one of its main attractions is camping. There are tourist companies that provide you with opportunities to camp in the middle of the desert. This camping is usually accompanied by a desert safari. 
                    
                    I visited Jaisalmer last year during the month of February. This is the time when the most awaited Desert Festival takes place. The vast stretches of desert turn into colorful fairgrounds for a few days. Various fun activities take place at this Festival. Camel races, turban tying, and Mr. Desert are few of the most famous activities.</p>
        </div>
        <img src="images/jaisalmer2.jpg" height="500px" width="900px" class="image">
        <div class="content">
            <p>
                    I had booked ourselves with a travel agency that gave us tents near the fair site. The tents were large enough to fit two people in one. Simple and white outside, they were tastefully decorated on the inside. The staff had provided us with everything that we could require during our stay. Once we reached the camping site, we were greeted with garlands. There was a group of musicians who were performing for us to introduce us to the Rajasthani culture. We were shown into our respective tents and we were asked to meet in the arrival ground an hour later. By the time I came out of my tent, few of the other visitors had gathered around. The sun bored into us with full might and I pulled my hat closer to my brow. Sometime later when we had all gathered together, we were taken to the fair. Each of us was assigned a camel along with a local who drove the camel. 
            </p>
        </div>
        <img src="images/jaisalmer.jpg" height="500px" width="900px" class="image">
        <div class="content">
            <p>
                    Rajasthani folk songs welcomed us to the fair. There was a splurge of colors all around. Starting with Rajasthani women dancing in their garb or the stalls, the fair was all about colors. We got down from our Camels and were led to the interior of the fair. Our guide delightfully explained to us about the origins of the fair and its significance. This annual fair is the one break that we all are looking for. The days and nights are filled with music and food, breaking the monotony of life. We had our lunch from one of the stalls. We were served an amazing Rajasthani dish called Dal-Batti churma which was just so flavourful that it opened us to a unique type of cuisine that was surely going to be missed from the city-style fast-tracked dishes.

                    When you are surrounded by sand on all sides, you are filled with this sense of dread. <br>
                    <br>My mind was filled with questions about how people survived in this heat and sand, all their lives. Our camels strolled in silence with the occasional grunts of other visitors. By the time we got down from the back of the camels, we were stiff. It took us some time to regain a straight back. All of us were made to sit in a circle and were served tea. Never had I imagined that a muggy desert could be so relaxing. We were enjoying ourselves. 
                    
                    It felt as if we were cut off from the rest of civilization. The peace and the quiet was ethereal. After we had had our tea we were brought back to our tents and rested for a while.
            </p>
        </div>
        <img src="images/jaisalmer4.jpg" height="500px" width="900px" class="image">
        <div class="content">
            <p>
                    The evening was a different scene altogether. While the troupe was resting, the staff had converted the area out front. They had cordoned off a part of the desert and decorated it with fairy lights. There was a bonfire burning in the middle. Slowly the other tent-mates started coming out of their tents. The group of musicians who had greeted us earlier started tuning melodies. All of us gathered around the fire and were served with refreshments. It felt nice sitting there and listening to the songs.<br> <br> The soulful voices of the singers struck a chord in my heart. As the evening progressed, the songs increased tempo too. Soon our body synced with the music and we all were dancing around the bonfire.

                    After about an hour and a half of dancing, we were served our dinner. The music started dwindling down. We thanked the musicians as they left and their grateful smiles spoke a lot. The party broke after a few minutes and the desert was silent as ever. I brought out a blanket from my room and wrapped myself in it. The clear sky was the perfect opportunity for me to stargaze. It was cold outside but the view made it worth it. The silence of the place and the stars overhead overwhelmed my heart with emotions. I went to sleep that night with a peaceful mind and a heavy heart. I had to face reality again, the next day.
            </p>
