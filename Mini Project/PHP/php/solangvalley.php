            <p>
                   <br><b> 5. Rohtang Pass<br></b>
Is your trip to Solang is incomplete without paying a visit to Rohtang Pass. One of the best snow points of Himachal is Rohtang Pass. The pass offers a natural gap between the Kullu Valley and the arid high altitude hills of Lahau and Spiti, with a hint of Buddhist culture. Ski, play in the snow or just enjoy the serenity that Rohtang Pass has to offer.
 
 
If paragliding scares the bejesus out of you, don’t worry Solang got you covered. A perfect spot to glide, cycle, soak in nature and take in all the spiritual positivity for your journey ahead.
