            <p>
                    <b>3. Kufri<br></b>
Kufri The name was given by locals and the meaning of Kufri is ‘lake’. scenic beauty with lots of mountains, and lake all in symphony to create a masterpiece that will imprint your memory forever. It won’t be wrong to say that if Heaven is on Earth, it’s on Kufri. During winter a meandering path through the potato plantations turns into a popular ski track.
            </p>
