            <p>
                  <b><br>  4. Shoolini Mata Mandir<br></b>
The temple is dedicated to goddess Shoolini, the area got its name based on the name of the goddess Shoolini. A Divine place to visit and worship. A very famous and important place to visit. Temple is small but nicely maintained. In the month of June, there is a week-long mela in the honor of Shoolani mata.
            </p>
